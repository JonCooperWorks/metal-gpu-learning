// Default workload: intentionally huge so users can see CPU/GPU crossover.
const DEFAULT_TOTAL_TRANSACTIONS: u64 = 2_000_000_000;
// Print progress every N% of chunks.
const DEFAULT_PROGRESS_INTERVAL: u64 = 10;
// For auto chunking, use at most this fraction of detected RAM budget.
const DEFAULT_MEMORY_FRACTION: f64 = 0.85;
// Decision boundary for classification: score >= threshold => predicted fraud.
const DEFAULT_THRESHOLD: f32 = 0.72;
// Number of score histogram buckets to show distribution shape.
const DEFAULT_SCORE_BINS: u32 = 20;
// Multiplier on hidden fraud prevalence (ground truth generation only).
const DEFAULT_FRAUD_RATE_BOOST: f32 = 1.0;
// Fallback chunk size when RAM detection is unavailable.
const DEFAULT_FALLBACK_CHUNK_TRANSACTIONS: u64 = 4_000_000;
// One threadgroup has 256 threads in both kernels.
// This is a common power-of-two size that simplifies tree reductions.
const THREADGROUP_SIZE: u64 = 256;
// Safety cap so threadgroup histogram arrays have a fixed compile-time size.
const MAX_SCORE_BINS: u32 = 64;

const SHADER_SOURCE: &str = include_str!("lesson7.metal");

#[derive(Clone, Copy, Debug, Eq, PartialEq)]
enum Mode {
    // Fill -> submit -> wait for each chunk in sequence.
    Single,
    // Two slots; CPU fills one while GPU may process the other.
    Double,
}

impl Mode {
    fn as_str(self) -> &'static str {
        match self {
            Self::Single => "single",
            Self::Double => "double",
        }
    }
}

#[derive(Clone, Copy, Debug, Eq, PartialEq)]
enum ValidationMode {
    // Recompute every chunk on CPU and compare with GPU outputs.
    Full,
    // Validate first and periodic chunks only (faster, still confidence).
    Spot,
    // Skip chunk-level CPU checks.
    Off,
}

impl ValidationMode {
    fn as_str(self) -> &'static str {
        match self {
            Self::Full => "full",
            Self::Spot => "spot",
            Self::Off => "off",
        }
    }
}

#[derive(Clone, Copy, Debug, Eq, PartialEq)]
enum CompareCpu {
    // Run a complete CPU baseline after GPU run for throughput comparison.
    On,
    // Skip CPU baseline timing.
    Off,
}

impl CompareCpu {
    fn as_str(self) -> &'static str {
        match self {
            Self::On => "on",
            Self::Off => "off",
        }
    }
}

#[repr(C)]
