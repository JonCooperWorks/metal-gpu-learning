// Teaching note:
// Small parser helper so parse errors can include the flag name.
fn parse_u64(name: &str, value: &str) -> Result<u64, String> {
    value
        .parse::<u64>()
        .map_err(|_| format!("Invalid value for {name}: '{value}'"))
}

// Teaching note:
